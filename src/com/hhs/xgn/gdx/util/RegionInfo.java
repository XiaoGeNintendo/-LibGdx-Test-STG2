package com.hhs.xgn.gdx.util;

public class RegionInfo {
	public String belongs;
	public String name;
	public int x,y,width,height;
	public RegionInfo(String belongs, String name, int x, int y, int width, int height) {
		this.belongs = belongs;
		this.name = name;
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
}
